package com.example.carouselsliderimage;

public class IconModelClass {

    private Integer icon;
    private String Icontext;

    public IconModelClass(Integer icon, String icontext) {
        this.icon = icon;
        Icontext = icontext;
    }

    public Integer getIcon() {
        return icon;
    }

    public String getIcontext() {
        return Icontext;
    }
}
